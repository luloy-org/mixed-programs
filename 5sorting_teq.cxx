#include <iostream>
#include <vector>

using namespace std;

//1. Bubble sort

/*Repeatedly steps through the list, compares adjacent elements, and swaps them if they're in the wrong order.
Simple but inefficient for large datasets.
Time Complexity: O(n²)*/
void bubbleSort(std::vector<int>& arr) {
    int n = arr.size();
    for (int i = 0; i < n - 1; i++) {
        for (int j = 0; j < n - i - 1; j++) {
            if (arr[j] > arr[j + 1]) {
                std::swap(arr[j], arr[j + 1]);
            }
        }
    }
}

/*2. Selection Sort

Divides the list into a sorted and unsorted part, repeatedly finding the minimum element from the unsorted part and moving it to the sorted part.
Inefficient for large datasets.
Time Complexity: O(n²)*/
void selectionSort(std::vector<int>& arr) {
    int n = arr.size();
    for (int i = 0; i < n - 1; i++) {
        int minIdx = i;
        for (int j = i + 1; j < n; j++) {
            if (arr[j] < arr[minIdx]) {
                minIdx = j;
            }
        }
        std::swap(arr[i], arr[minIdx]);
    }
}

/*3. Insertion Sort

Builds the sorted array one item at a time by inserting elements into their correct position.

Efficient for small or nearly sorted datasets.

Time Complexity: O(n²)*/
void insertionSort(std::vector<int>& arr) {
    int n = arr.size();
    for (int i = 1; i < n; i++) {
        int key = arr[i];
        int j = i - 1;
        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j];
            j--;
        }
        arr[j + 1] = key;
    }
}

/*4. Merge Sort

A divide-and-conquer algorithm that splits the array into halves, sorts each half, and then merges them back together in sorted order.

Stable and efficient for large datasets.

Time Complexity: O(n log n)
*/

void merge(std::vector<int>& arr, int left, int mid, int right) {
    int n1 = mid - left + 1;
    int n2 = right - mid;
    std::vector<int> L(n1), R(n2);

    for (int i = 0; i < n1; i++) L[i] = arr[left + i];
    for (int i = 0; i < n2; i++) R[i] = arr[mid + 1 + i];

    int i = 0, j = 0, k = left;
    while (i < n1 && j < n2) {
        if (L[i] <= R[j]) arr[k++] = L[i++];
        else arr[k++] = R[j++];
    }

    while (i < n1) arr[k++] = L[i++];
    while (j < n2) arr[k++] = R[j++];
}

void mergeSort(std::vector<int>& arr, int left, int right) {
    if (left < right) {
        int mid = left + (right - left) / 2;
        mergeSort(arr, left, mid);
        mergeSort(arr, mid + 1, right);
        merge(arr, left, mid, right);
    }
}

/*5. Quick Sort

A divide-and-conquer algorithm that selects a pivot element and partitions the array around the pivot, recursively sorting the subarrays.

Fast and widely used but can degrade to O(n²) if poorly implemented.

Average Time Complexity: O(n log n)*/
int partition(std::vector<int>& arr, int low, int high) {
    int pivot = arr[high];
    int i = low - 1;
    for (int j = low; j < high; j++) {
        if (arr[j] < pivot) {
            std::swap(arr[++i], arr[j]);
        }
    }
    std::swap(arr[i + 1], arr[high]);
    return i + 1;
}

void quickSort(std::vector<int>& arr, int low, int high) {
    if (low < high) {
        int pi = partition(arr, low, high);
        quickSort(arr, low, pi - 1);
        quickSort(arr, pi + 1, high);
    }
}
